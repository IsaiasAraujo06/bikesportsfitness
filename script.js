function irParaCatalogo() {
    document.getElementById("home").style.display = "none";
    document.getElementById("catalogo").style.display = "block";
}

function filtrarProdutos() {
    let input = document.getElementById("searchInput").value.toLowerCase();
    let catalogo = document.getElementById("catalogo");
    let lista = document.getElementById("listaProdutos").getElementsByTagName("li");
    let resultadosDiv = document.getElementById("resultadosPesquisa");
  
    resultadosDiv.innerHTML = "";
  
    if (input === "") {
      catalogo.style.display = "block"; 
      return;
    }
  
    let resultados = [];
  
    for (let item of lista) {
      if (item.textContent.toLowerCase().includes(input)) {
        resultados.push(item.textContent);
      }
    }
  
    if (resultados.length > 0) {
      catalogo.style.display = "none"; 
      let ul = document.createElement("ul");
      resultados.forEach((produto) => {
        let li = document.createElement("li");
        li.textContent = produto;
        ul.appendChild(li);
      });
      resultadosDiv.appendChild(ul);
    } else {
      catalogo.style.display = "block";
    }
  }
  

function buscarProdutos(event) {
    if (event && event.key === "Enter") {
        event.preventDefault();
    }
}

    const termoBusca = document.getElementById("search-input").value.toLowerCase();
    const produtos = document.querySelectorAll(".card");
    let encontrou = false;

    produtos.forEach(produto => {
        const nomeProduto = produto.querySelector("h2").textContent.toLowerCase();

        if (nomeProduto.includes(termoBusca)) {
            produto.style.display = "block";
            encontrou = true;

            const regex = new RegExp(`(${termoBusca})`, "gi");
            produto.querySelector("h2").innerHTML = nomeProduto.replace(regex, "<mark>$1</mark>");
        } else {
            produto.style.display = "none";
        }
    });

    document.getElementById("mensagem-erro").style.display = encontrou ? "none" : "block";
